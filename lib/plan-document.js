/**
 * Format an implementation plan as markdown for storage (Gist) and Linear.
 */

function formatPlanMarkdown(plan) {
  const lines = [
    `# Implementation plan: ${plan.issueIdentifier}`,
    '',
    `**Summary:** ${plan.summary || plan.issueTitle || 'Untitled'}`,
    `**Linear:** [${plan.issueIdentifier}](${plan.issueUrl})`,
    `**Repo:** \`${plan.repo}\``,
    `**Branch:** \`${plan.branch}\``,
    `**Created by:** ${plan.createdBy || 'Unknown'}`,
    `**Plan ID:** \`${plan.id}\``,
    '',
    '## Linear issue',
    '',
    plan.issueTitle ? `**${plan.issueTitle}**` : null,
    plan.issueUrl || null,
    '',
    '### Description',
    '',
    plan.issueDescription || '_No description_',
    '',
    '## Steps',
    '',
    ...(plan.steps || []).map((step, i) => `${i + 1}. ${step}`),
    '',
    '## Pull request (planned)',
    '',
    `**Title:** ${plan.prTitle || ''}`,
    '',
    plan.prBody || '',
    '',
    '---',
    '',
    '_Generated by Discord → Linear bot. Stored as a Linear document on the issue. Reply **go** in Discord to execute._',
  ].filter((line) => line !== null);

  return lines.join('\n');
}

function gistFilenameForPlan(plan) {
  return `${(plan.issueIdentifier || 'plan').toLowerCase()}-implementation-plan.md`;
}

module.exports = {
  formatPlanMarkdown,
  gistFilenameForPlan,
};
